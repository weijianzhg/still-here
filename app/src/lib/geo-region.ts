export function regionIdFromCountryCode(code: string | null | undefined): GeoRegionId | null {
  if (!code || code.length !== 2) return null;
  const c = code.toUpperCase();
  if (!/^[A-Z]{2}$/.test(c)) return null;

  if (NORTH_AMERICA.has(c)) return "north-america";
  if (SOUTH_ASIA.has(c)) return "south-asia";
  if (LATIN_AMERICA.has(c)) return "latin-america";
  if (MENA.has(c)) return "mena";
  if (SUB_SAHARAN_AFRICA.has(c)) return "sub-saharan-africa";
  if (EUROPE_CENTRAL_ASIA.has(c)) return "europe-central-asia";
  if (EAST_ASIA_PACIFIC.has(c)) return "east-asia-pacific";

  return null;
}
